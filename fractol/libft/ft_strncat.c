#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t		i;
	size_t		j;

	i = ft_strlen(s1);
	j = 1;
	if (n > 0)
	{
		s1[i] = s2[0];
		i++;
		while (s2[j] != '\0' && j < n)
		{
			s1[i] = s2[j];
			j++;
			i++;
		}
		s1[i] = '\0';
	}
	return (s1);
}
