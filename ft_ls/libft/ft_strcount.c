#include "libft.h"

int		ft_strcount(char const *s, char c)
{
	int		i;
	int		j;
	int		k;

	i = 0;
	j = 0;
	k = 0;
	while (s[i])
	{
		if (j == 0 && s[i] != c)
		{
			j = 1;
			k++;
		}
		if (s[i] == c)
			j = 0;
		i++;
	}
	return (k);
}
